const { data: cats, error } = await supabase
  .from("cats")
  .select("id, name, description")
  .is("caretaker_id", null);

if (error) {
  console.error("❌ 조회 실패:", error.message);
  process.exit(1);
}

console.log(`📋 더미 고양이 ${cats.length}마리 발견.\n`);

let updated = 0;
let failed = 0;

for (let i = 0; i < cats.length; i++) {
  const cat = cats[i];
  const newDesc = GENERIC_DESCRIPTIONS[i % GENERIC_DESCRIPTIONS.length];

  const { error: updateErr } = await supabase
    .from("cats")
    .update({ description: newDesc })
    .eq("id", cat.id);

  if (updateErr) {
    console.error(`❌ ${cat.name}: ${updateErr.message}`);
    failed++;
  } else {
    console.log(`✓ ${cat.name}`);
    updated++;
  }
}

